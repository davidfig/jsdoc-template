exports.handlers = {
    processingComplete: function (e)
    {
        for (let i = 0; i < e.doclets.length; i++)
        {
            const doclet = e.doclets[i]
            if (!doclet.undocumented && doclet.meta && doclet.meta.path.indexOf('node_modules') !== -1)
            {
                // doclet.isNodeModule = true
                doclet.longname += '~'
                console.log(doclet)
            }
        }
    }
};